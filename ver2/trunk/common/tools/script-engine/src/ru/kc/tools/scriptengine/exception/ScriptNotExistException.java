package ru.kc.tools.scriptengine.exception;

public class ScriptNotExistException extends Exception {

	public ScriptNotExistException() {
		super();
	}

	public ScriptNotExistException(String message, Throwable cause) {
		super(message, cause);
	}

	public ScriptNotExistException(String message) {
		super(message);
	}

	public ScriptNotExistException(Throwable cause) {
		super(cause);
	}

	
	
}
