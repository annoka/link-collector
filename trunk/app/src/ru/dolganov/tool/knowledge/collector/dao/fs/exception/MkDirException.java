package ru.dolganov.tool.knowledge.collector.dao.fs.exception;

public class MkDirException extends Exception{

	public MkDirException() {
		super();
	}

	public MkDirException(String message, Throwable cause) {
		super(message, cause);
	}

	public MkDirException(String message) {
		super(message);
	}

	public MkDirException(Throwable cause) {
		super(cause);
	}

}
